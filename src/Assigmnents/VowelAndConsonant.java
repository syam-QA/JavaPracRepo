public class VowelAndConsonant 
{

	

	public static void main(String[] args) 
	{
		// WAP to check given alphabet character is Vowel or Consonant using Switch - Case
		
	char s='@';
		
	switch (s)
	{
	
	case 'a':
	case 'A':
		System.out.println("vowel");
		break;
	case 'e':
	case 'E':
		System.out.println("vowel");
		break;
	case 'i':
	case 'I':
		System.out.println("vowel");
		break;
	case 'o':
	case 'O':
		System.out.println("vowel");
		break;
	case 'u':
	case 'U':
		System.out.println("vowel");
		break;
	default :
			System.out.println("Consonant");
	break;		
	}
	
	

	}

}
